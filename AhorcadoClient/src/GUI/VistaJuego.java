    private void enviarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarButtonActionPerformed
        // Obtener la propuesta del jugador
		String propuesta = entradaLetrasTextField.getText().trim();
		
		if (propuesta.length() > 1){
			// La propuesta es una palabra completa
			cliente.proponerPalabra(propuesta);
		}
		else if (propuesta.length() == 1){
			// La propuesta es una letra
			cliente.proponerLetra(propuesta);
		}
		
		entradaLetrasTextField.setText("");
    }//GEN-LAST:event_enviarButtonActionPerformed

