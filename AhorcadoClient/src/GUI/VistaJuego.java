	public void nuevoJuego(){
		int vidas = cliente.getVidas();
		vidasTextLabel.setText("" + vidas); //! @todo arreglar esto
		
		String nombreJugador = cliente.getNombre();
		nombreJugadorTextLabel.setText("" + nombreJugador);
		
		int numLetras = cliente.getNumLetras();
		letrasNoValidasTextLabel.setText("<html>");
		palabrasNoValidasTextLabel.setText("<html>");
		
		palabraTextLabel.setText("");
		for (int i = 0; i < numLetras; ++i)
			palabraTextLabel.setText(palabraTextLabel.getText() + "_ ");
		
		cliente.iniciarEscuchaPartida(this);
	}
	
	public void actualizarPalabra(String palabraDescubierta){
		palabraTextLabel.setText("");
		for (int i = 0; i < palabraDescubierta.length(); ++i)
			palabraTextLabel.setText(palabraTextLabel.getText() + palabraDescubierta.charAt(i) + " ");
	}
	
	public void nuevaLetraNoValida(String letra){
		if (!letrasNoValidasTextLabel.getText().substring(6).contains(letra))
			letrasNoValidasTextLabel.setText(letrasNoValidasTextLabel.getText() + letra + ", ");
	}
	
	public void nuevaPalabraNoValida(String palabra){
		if (!palabrasNoValidasTextLabel.getText().contains(palabra + ","))
			palabrasNoValidasTextLabel.setText(palabrasNoValidasTextLabel.getText() + palabra + ", ");
	}
	
	public void actualizarVidas(String vidas){
		vidasTextLabel.setText(vidas);
	}
	
	public void terminarPartida(String adivinador, String palabra){
		JOptionPane.showMessageDialog(this, "¡" + adivinador + " ha adivinado la palabra! Era \"" + palabra + "\".");
		cliente.terminarPartida();
	}
	
	public void terminarPartida(String palabra){
		JOptionPane.showMessageDialog(this, "¡Habéis perdido! La palabra era \"" + palabra + "\".");
		cliente.terminarPartida();
	}

